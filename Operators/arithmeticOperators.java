package Operators;

public class arithmeticOperators {

	public static void main(String[] args) {
		
		int a = 4;
		int b = 3;
		
		int c = a+b;
		
		System.out.println(c);
	
		int r = 4;
		int s = 8;
		
		int t = s-r;
		
		System.out.println(t);
		
		int u = 5;
		int v = 4;
		
		int w = u*v;
		 
		System.out.println(w);
		
		int x = 6;
		int y = 8;
		
		float z = (float) x/y;
		
		System.out.println(z);
		
		int p = 8;
		int q = 3;
		
		int m = p%q;
		
		System.out.println(m);

	}

}
